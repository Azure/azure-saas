.NET Foundation CLA
The 
.NET Foundation is a 
501(c)(6)
non-profit organization which was established 
to support an innovative commercially friendly, open-source ecosystem around the .NET platform.
While Microsoft assists with infrastructure alignment, the foundation maintains a separate CLA. You can read more about this process at https://cla.dotnetfoundation.org/. Even Microsoft employees must sign the .NET Foundation CLA to cover their contributions to the community.
Corporate CLA (CCLA)
Microsoft does not typically participate in corporate
CLA agreements given the scale and logistics challenge.
However, we recognize that some companies
especially those in highly regulated industries
need to have the ability to explicitly manage
specific people who have access to participate in Microsoft projects
To enable these scenarios
an authorized individual or group from such a company can receive a 
special "CCLA repository" that will allow your firm to manually manage specific GitHub logins that are authorized on behalf of your company. You can also use GitHub Actions or other technology to customize and automate this list.
A CCLA repository is a bespoke process.
If you feel your company is a good fit for
this please reach out to the Microsoft Open 
Source Programs Office at <opensource@microsoft.com>
